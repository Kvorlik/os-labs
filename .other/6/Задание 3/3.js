/*сценарий JScript, рассчитывающий суму n первых чисел*/
/*ряда при помощи рекурсивной функции. Ряд задается рекуррентно:*/
/*F(1)=1,*/
/*F(i)=ln(f(i-1)*i)+9*/
/**********************************************************/
function Recurs(i)
{
	if(i == 1) 
	{
		return 1;
	}
	else 
	{
		return Math.log(Recurs(i-1)*i)+9;
	}
}